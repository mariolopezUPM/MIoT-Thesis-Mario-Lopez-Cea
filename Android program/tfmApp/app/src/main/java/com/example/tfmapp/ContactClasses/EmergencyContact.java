package com.example.tfmapp.ContactClasses;

public class EmergencyContact {

    private String name;
    private String id;

    public EmergencyContact(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }


}
